"""
Маршруты (URL-адреса) для приложения тестирования.

Этот файл определяет все URL-адреса, которые пользователь может посетить
во время прохождения теста на определение уровня английского языка.

Основные маршруты:
- intro: приветственная страница перед тестом
- start: запуск новой тестовой сессии
- question: отображение вопросов и обработка ответов
- finish: досрочное завершение теста (минимум 10 вопросов)
- timeout: обработка истечения времени (30 минут)
- results: отображение результатов теста
"""

from django.urls import path
from . import views

# Название приложения для использования в шаблонах (например: {% url 'user_test:intro' %})
app_name = 'user_test'

urlpatterns = [
    # Приветственная страница с информацией о тесте
    path('', views.test_intro_view, name='intro'),
    
    # Создание новой тестовой сессии и запуск теста
    path('start/', views.start_test_view, name='start'),
    
    # Основная страница теста - отображение вопросов и обработка ответов
    path('question/', views.test_question_view, name='question'),
    
    # Досрочное завершение теста (если пользователь ответил минимум на 10 вопросов)
    path('finish/', views.finish_test_view, name='finish'),
    
    # Страница таймаута (показывается когда истекли 30 минут на тест)
    path('timeout/', views.timeout_view, name='timeout'),
    
    # Детальные результаты теста с определенным уровнем и рекомендациями
    path('results/<int:session_id>/', views.test_results_view, name='results'),
]
